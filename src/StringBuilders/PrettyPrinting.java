package StringBuilders;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.12745f;
        System.out.printf("formatted number is %.2f   ",a);//it prints unto 2 decimal points and rounds off
        System.out.printf("Pie : %.3f",Math.PI);// it prints unto 3 decimal points
        System.out.println();

        System.out.printf("Hello my name is %s and iam %s","kunal","cool");


    }
}
